classdef GRELM
    properties
        numberOfHiddenNeurons = 1000        
        regularizationParameter = 1000
        activationFunction = 'sig'
        numberOfInputNeurons = []
        inputWeight = []
        biasOfHiddenNeurons = []
        outputWeight = []
        seed = []
        maxIter = 20
        alpha = 0.5
    end
    methods
        function obj = GRELM(varargin)
            for i = 1:2:nargin
                obj.(varargin{i}) = varargin{i+1};
            end
            if isnumeric(obj.seed) && ~isempty(obj.seed)
                obj.seed = RandStream('mt19937ar','Seed', obj.seed);
            elseif ~isa(obj.seed, 'RandStream')
                obj.seed = RandStream.getGlobalStream();
            end
            if isempty(obj.numberOfInputNeurons)
                throw(MException('GRELM:emptynumberOfInputNeurons','Empty Number of Input Neurons'));
            end
            obj.inputWeight = rand(obj.seed, obj.numberOfInputNeurons, obj.numberOfHiddenNeurons)*2-1;
            obj.biasOfHiddenNeurons = rand(obj.seed, 1, obj.numberOfHiddenNeurons);
            
            if isequal(class(obj.activationFunction),'char')
                switch lower(obj.activationFunction)
                    case {'sig','sigmoid'}
                        %%%%%%%% Sigmoid
                        obj.activationFunction = @(tempH) 1 ./ (1 + exp(-tempH));
                    case {'sin','sine'}
                        %%%%%%%% Sine
                        obj.activationFunction = @(tempH) sin(tempH);
                    case {'hardlim'}
                        %%%%%%%% Hard Limit
                        obj.activationFunction = @(tempH) double(hardlim(tempH));
                    case {'tribas'}
                        %%%%%%%% Triangular basis function
                        obj.activationFunction = @(tempH) tribas(tempH);
                    case {'radbas'}
                        %%%%%%%% Radial basis function
                        obj.activationFunction = @(tempH) radbas(tempH);
                        %%%%%%%% More activation functions can be added here
                end
            elseif ~isequal(class(obj.activationFunction),'function_handle')
                throw(MException('GRELM:activationFunctionError','Error Activation Function'));
            end
        end
        function self = train(self, X, Y)
            tempH = X*self.inputWeight + repmat(self.biasOfHiddenNeurons,size(X,1),1);
            clear X;
            H = self.activationFunction(tempH); 
            H = normr(H);
            clear tempH;
            
            [N, Ntil] = size(H);
            m = size(Y,2);

            shrinkage = @(B) bsxfun(@times,max(0,1-self.alpha./sqrt(sum(B.^2,2))),B);
            
            % cholesky decomposition
            eta = (((1-self.alpha)+1)/self.regularizationParameter);
            if ( N >= Ntil )
                L = chol( H'*H + eta*speye(Ntil), 'lower' );
            else
                L = chol( speye(N) + 1/eta*(H*H'), 'lower' );
            end
            L = sparse(L);
            LS = sparse(L');
            
            % save a matrix-vector multiply
            Htb = H'*Y;

            % ADMM solver            
            Z = zeros(Ntil,m);
            U = zeros(Ntil,m);                        
            for k = 1:self.maxIter
                % B-update
                q = Htb + (1/self.regularizationParameter)*(Z - U); % temporary value
                if( N >= Ntil )
                    B = LS \ (L \ q);
                else       
                    B = q/eta - (H'*(LS \ ( L \ (H*q) )))/eta^2;
                end                                
                Z = shrinkage(B + U); % Z-update                               
                U = U + (B - Z);  % U-update                
            end 
            
            idxAct = any(Z,2);
            self.inputWeight = self.inputWeight(:,idxAct);
            self.biasOfHiddenNeurons = self.biasOfHiddenNeurons(idxAct);
            self.outputWeight = Z(idxAct,:);
            self.numberOfHiddenNeurons = sum(idxAct);
        end    
        
        function Yhat = predict(self, X)
            tempH = X*self.inputWeight + repmat(self.biasOfHiddenNeurons,size(X,1),1);
            clear X;
            H = self.activationFunction(tempH); 
            H = normr(H);
            Yhat = H * self.outputWeight;
        end       
    end
end
